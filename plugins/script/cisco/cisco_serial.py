import re


# 序列号
def get_cisco_serial(data):
    serial_dict = {
        "cisco_serial": {}
    }

    # 判断是否真的采集
    if 'No Such' not in data:
        for i in str(data).split('\n'):
            if i:
                serial = re.search(r'mib-2.47.1.1.1.1.11.(\d+)\s+=\s+(.*)', i)
                serial_num = serial.group(1)  # 1061 1062
                serial_sn = serial.group(2)  # LIT23153FCY
                if "STRING" in serial_sn:
                    serial_sn = eval(serial_sn.split(' ')[1])
                    one_serial = {serial_num: serial_sn}
                    serial_dict['cisco_serial'].update(one_serial)
                    break

    return serial_dict






